const calculatorFunctions = {

    addOperator: (num1, num2) => {
        return num1 + num2;
    },

    subtractOperator: (num1, num2) => {
        return num1 - num2
    },
    
    multiplyOperator: (num1, num2) => {
        return num1 * num2;
    },

    divideOperator: (num1, num2) => {
        return num1 / num2;
    }

};

export default calculatorFunctions;